#include <stdio.h>

/**
 *print_most_numbers(void) will print numbers from 1 to 9 except 2 and 4
 *@i: The integer to check
 * Return: Always 0.
 */
void print_most_numbers(void)
{
	for (int i=0; i < 10; i++)
	{
		if (i == 4 || i == 2)
		{
		       continue;	
		}
	return (0);
	}
}
